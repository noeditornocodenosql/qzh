package com.qianhe.model;

public class Menu {
	private Integer id;
	private Integer type;
	private String menuFirst;
	private String menuTwo;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getMenuFirst() {
		return menuFirst;
	}

	public void setMenuFirst(String menuFirst) {
		this.menuFirst = menuFirst;
	}

	public String getMenuTwo() {
		return menuTwo;
	}

	public void setMenuTwo(String menuTwo) {
		this.menuTwo = menuTwo;
	}

}
